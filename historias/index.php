						<h2 class="blog-post-title">O jogo de futebol</h2>
						<p class="blog-post-meta">Escrito por Adriano Mantovani Armelim, em 4 de Maio de 2020.</p>
						<hr>
						<p>Quando criança as brincadeiras eram mais simples, na rua, nos terrenos baldios, nos becos e nos córregos da região em que morávamos em Sorocaba, no interior de São Paulo. Sempre ali por perto. As vezes um pouco mais longe, na casa de um ou outro amigo.</p>
						<p>Gostávamos de várias brincadeiras mas jogar futebol era a principal. Jogávamos na rua, entre os amigos mesmo. Sempre fazíamos 2, 3, 4 times. A escolha dos times era quase democrática. Dividíamos as crianças em 3 grupos distintos, o grupo dos "grandes", o grupo dos "médios" e o grupo dos "pequenos". Com essa divisão conseguíamos equilibrar os times não permitindo, na maioria das vezes, que duas crianças  do mesmo grupo fossem escolhidas para o mesmo time na sequencia. Isso seria injusto. 2, 3 ou 4 dos grandes sempre faziam as escolhas dos times. Os gols, lógico, eram feitos com chinelo ou com algum pedaço de tijolo ou madeira que estivesse ali por perto.</p>
						<p>Certa vez um vizinho que morava em uma casa ali próximo, o "Gordo" (nem sei porque ele tinha esse apelido, afinal ele não era nada gordo.) e que sempre nos via jogando por ali, nos chamou para um campeonato que ia acontecer em um terreno baldio ali por perto.</p>
						<p>Eu conhecia o campo (quer dizer, o terreno baldio), passava por ele todos os dias quando ia pegar o ônibus pra ir pra escola ou quando ia comprar pão na padaria. Ficava a uns 4 quarteirões da minha casa. Era um terreno que servia de caminho pra todos os lugares. Servia de atalho. Nunca havia me passado pela cabeça que aquele local pudesse ser usado para um campo de futebol, ainda mais para um campeonato tão importante. Cheguei a pensar comigo, naquele exato momento, que talvez a "trilha" que havia no meio (o caminho) pudesse atrapalhar o bom andamento do jogo. </p>
						<p>Claro que aceitamos o convite na hora e iniciamos naquele momento uma série de jogos amistosos para nos prepararmos para os grandes jogos do campeonato, que começaria dali  a 4 semanas.</p>
						<p>Os jogos da 1a fase foram todos no mesmo final de semana. Apenas 4 times se inscreveram, todos jogaram contra todos. Alguns jogos eram intensos e disputados. Outros nem tanto. Os  dois melhores times chegaram a grande final. O nosso time,  surpreendentemente, foi um desses dois. O outro, foi o time do "Gordo".</p>
						<p>Pra gente chegar a final não tinha sido uma grande surpresa, afinal, só havíamos escolhido os melhores para participar do time, os "grandes" . Alguns médios até ficaram do banco de reserva, mas os pequenos não tiveram nenhuma chance na convocação. Ficaram mesmo foi para a torcida.</p>
						<p>A grande final foi marcada para o próximo sábado. O prêmio para o campeão seriam algumas taubainas e, para o vice, um pouco menos de tubainas. Iamos beber juntos na padaria que ficava ali por perto, logo ao final da partida.</p>
						<p>Durante aquela semana não se falava em outra coisa no bairro. Como seria o grande jogo, quem será que seria o juiz, com qual bola iríamos jogar, quanto tempo teria o jogo, será que não seria bom termos um técnico? Entre outras grandes conjecturas...</p>
						<p>O time dos caras era bom. O "Gordo" era o melhor jogador do bairro. No entanto, realmente achávamos que tínhamos alguma chance . No 1o turno havíamos perdido deles apenas por 2 a 0. Um placar apertado. Um jogo disputado. E o melhor, nem estávamos com a escalação completa naquela partida.</p>
						<p>Ainda durante a semana algo aconteceu comigo que me deixou muito ansioso e preocupado. Algumas bolinhas brancas começaram a surgir nas minhas duas pernas. Coçava muito. A preocupação estava toda relacionada ao jogo. Será que eu poderia participar com aquilo? Será que melhoraria até lá? Ou será que ficaria muito pior? </p>
						<p>Logo no início da semana minha mãe marcou médico e me levou para a consulta. Ao passar pela consulta recebi uma triste notícia. Eu precisaria passar por um "procedimento" que consistia em retirar cada bolinha de cada uma das pernas, uma por uma, com uma pequena ferramenta médica. Um trabalho delicado, dolorido e demorado. Aquilo me deixou mais preocupado ainda. Minha preocupação só aumentou quando minha mãe me deu a notícia avisando para quando havia conseguido marcar o maldito procedimento. Lógico, claro. No sábado da grande final!!!</p>
						<p>Eu não podia acreditar. Briguei com minha mãe. Fiquei bem irritado aquele dia e mais o outro. Mas aquela irritação não durou muito não, afinal de contas aquelas bolinhas realmente estavam me incomodando. A coceira começou a aumentar e algumas vezes começou até a doer.</p>
						<p>Meus amigos também ficaram preocupados, começamos a conversar sobre qual dos "médios" poderia ser meu substituto. Acabamos escolhendo um entre os melhores mas nenhum estaria a altura daquela responsabilidade de participar da grande final. E agora? Será que não seria melhor desistirmos logo?</p>
						<p>O procedimento estava marcado para as 14 horas. A previsão era que demorasse mais ou menos uma hora para ser realizado. Diante dessa informação, consegui, ao longo da semana, numa grande disputa de bastidores mudar o horário de início do jogo. Ao invés de iniciar as 14 da tarde, o jogo agora teria início as 16. Pelos meus cálculos dessa forma daria tempo de eu fazer o procedimento e ainda voltar para participar da partida. Claro que o time do "Gordo" fez de tudo para que isso não ocorrece mas em algum momento acabaram cedendo.</p>
						<p>Fui ao médico naquele dia já brigando com minha mãe e na sala de espera continuei a brigar com ela. Ela não queria de jeito nenhum deixar eu participar da partida logo depois de ter saído de um procedimento médico. Tudo piorou quando mostramos minha intenção para o médico. Ele foi enfático: "Não é recomendado não, passamos uns produtos, a região ficará sensível, bla, bla bla" Eu nem escutava. Ainda mais porque queria que ele começasse logo com aquilo.</p>
						<p>O procedimento médico foi um sucesso. Com exceção do horário em que acabou. 15:50. Do médico até o campo demoraríamos meia hora, ou seja eu, com certeza, chegaria atrasado. Acelerei muito minha mãe pra irmos embora. Ela percebeu minha agitação e acabou cedendo, permitindo que eu participasse da partida. Ela me deixaria na frente do campo. Ufa!!</p>
						<p>Estacionamos o carro por volta de 16:20 bem em frente ao campo. A torcida cobria toda a lateral do "gramado". Tive que pedir algumas licensas para conseguir transpor aquele paredão de gente. Nas minhas pernas eu ainda trazia um monte de curativos brancos que fui arrancando um a um assim que desci do carro. Lógico que já havia colocado todo o uniforme dentro do carro mesmo, no caminho. Então cheguei gritando para que o "médio" que estava jogando no meu lugar saísse. Ele exitou um pouco, mas como já estávamos perdendo por um a zero foi expulso pelos outros jogadores do nosso time, assim que eles me viram se aproximando.</p>
						<p>Quando cheguei vi que o juiz que havia sido escolhido para a partida era um ex jogador do time adversário. Aquilo não me parecia certo. Mas não havia tempo para discutir sobre aquilo naquele momento. Eu precisava entrar no jogo. Um pouco depois de eu ter entrado em campo já começamos a jogar um pouco melhor mas, com a confusão de minha chegada, acabamos tomando o segundo gol.</p>
						<p>No intervalo conversamos, eles me passaram as preocupações os motivos obscurors da escolha do juiz , entre outros assuntos inúteis. Um jogador de um outro time que havia sido eliminado estava sendo nosso técnico naquela partida. Será que aquilo era permitido pelo regulamento? Será que havia um regulamento?</p>
						<p>Bem, logo no início do 2o tempo deu pra perceber que nosso time estava melhor. Tivemos algumas jogadas de perigo e, faltando 15 minutos para o fim do jogo conseguimos diminuir o placar.</p>
						<p>Muitas brigas, muitas jogadas e, de repente, para nossa surpresa o juiz apita o final de jogo. E ele fez isso faltando ainda 5 minutos para o jogo acabar. Ficamos revoltados, a briga dentro e fora de campo foi generalizada. Muitas pessoas gritando, muitos tentando partir pra agressão.</p>
						<p>Apenas nesse momento, no apito final, é que comecei a perceber que minha perna estava totalmente vermelha nos locais onde antes haviam os curativos brancos.</p>
						<p>Depois de um certo tempo acabamos aceitando o resultado e indo com todos para a padaria tomar nossas tubainas. Esse foi o único ano em que tivemos aquele campeonato. Não havia como manter um evento daquele porte em todos os anos. Ali pude fazer novas amizades e, até hoje, tenho algumas cicatrizes incuradas nas duas pernas por conta desse grande jogo na final.</p>
